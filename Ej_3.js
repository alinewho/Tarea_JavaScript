//Haz un javascript para: Calcular el área de un triángulo. ¿Qué variables necesitas? 
//Los valores deben de ser capturados con promt y mostrados con alertas.


let base = prompt("Ingrese la base del triángulo");
let altura = prompt("Ingrese la altura del triángulo");
let area = base * altura / 2;
alert("El area del triangulo es: " + area);
